var price = 35;
// price = price + 10;
price += 10; // Using shorthand operator(+=) to add 10 to price
price -= 5; // Using shorthand operator(-=) to subtract 5 from price
console.log(price); 
console.log(price - 5); // 35, using shorthand operator(-=) to subtract 5 from price...variales will always return the last value assigned to it

var fridge = 35000;
//fridge = fridge - 1000; Subtracting 1000 from fridge price
fridge -= 1000; // Using shorthand operator(-=) to subtract 1000 from fridge price
console.log(fridge); // 34000, using shorthand operator(-=) to subtract

